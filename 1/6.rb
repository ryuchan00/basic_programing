def kadai6(a, b, op)
  printf("%.3g\n", eval("#{a} #{op} #{b}"))
  printf("%.20g\n", eval("#{a} #{op} #{b}"))
  printf("%.30g\n", eval("#{a} #{op} #{b}"))
end

# 桁落ち
kadai6(1.2, 1.0, '-')
kadai6(1.12345, 1.0, '-')
kadai6(1.1234512345, 1.12345, '-')

# 情報落ち
kadai6(1.0, 0.0012345,'+')
kadai6(1.0, 0.00000000012345,'+')
kadai6(1.0, 0.000000000000000000012345,'+')